import java.util.ArrayList;

public class IntroArrayLists {
	public static void main(String[] args) {
		ArrayList <String> StringList = new ArrayList<String>();
		StringList.add("Dave");
		StringList.add("Evan");
		StringList.add("Alec");
		for (String s:StringList) {
			System.out.println(s);
		}
	}
}
