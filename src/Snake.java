
public class Snake {
private int Viciousness;
private boolean isVenemous;
Snake(int Viciousness, boolean isVenemous){
	this.Viciousness = Viciousness;
	this.isVenemous = isVenemous;
}
int getViciousness(){
	
	return Viciousness;	
}
void setViciousness(boolean isVenemous){
		this.Viciousness = Viciousness;
}
boolean getisVenemous(){
	
	return isVenemous;	
}
void setisVenemous(boolean isVenemous){
		this.isVenemous = isVenemous;
}
}
